CREATE  TABLE movie_details
(
    show_id VARCHAR(255),
    type VARCHAR(255),
    title VARCHAR(255),
	director VARCHAR(255),
    cast VARCHAR(255),
	country VARCHAR(255),
	date_added VARCHAR(255),
    release_year INTEGER,
	rating VARCHAR(255),
	duration VARCHAR(255),
	listed_in VARCHAR(255),
	description VARCHAR(255),
	PRIMARY KEY (show_id)
);